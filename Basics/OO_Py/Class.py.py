class Employee:
    def __init__(self, memberID, name, salary):
        self.memberID = memberID
        self.name = name
        self.salary = salary

    def Display_Emp(self):
        print("Name of the Employee is " + self.name)
        print("The member ID of the Employee is: " + self.memberID)
        print("The salary of the employee is: " + self.salary)


